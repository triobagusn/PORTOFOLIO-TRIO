const config = window.PORTFOLIO_CONFIG;
let language = localStorage.getItem("portfolio-language") || "en";
let activeFilter = "all";

const projectsGrid = document.getElementById("projectsGrid");
const modal = document.getElementById("projectModal");
const modalDialog = modal.querySelector(".modal-dialog");

function createProjectCard(project) {
  const content = project[language];
  const tags = project.stack.slice(0, 4).map(tag => `<span>${tag}</span>`).join("");
  return `
    <article class="project-card accent-${project.accent} reveal" tabindex="0" data-project="${project.id}" data-category="${project.category.join(" ")}" aria-label="Open case study: ${content.title}">
      <div class="project-card-top"><span class="project-number">${project.number}</span><span class="project-open">↗</span></div>
      <div class="project-preview">
        <div class="preview-window">
          <div class="preview-toolbar"><i></i><span>${project.icon}</span></div>
          <div class="preview-kpis"><i></i><i></i><i></i></div>
          <div class="preview-body"><div class="preview-chart"></div><div class="preview-list"></div></div>
        </div>
      </div>
      <span class="project-label">${content.label}</span>
      <h3>${content.title}</h3>
      <p>${content.subtitle}</p>
      <div class="project-tags">${tags}</div>
    </article>`;
}

function renderProjects() {
  projectsGrid.innerHTML = config.projects.map(createProjectCard).join("");
  bindProjectCards();
  applyFilter(activeFilter);
  observeReveals();
}

function bindProjectCards() {
  document.querySelectorAll(".project-card").forEach(card => {
    const open = () => openProject(card.dataset.project);
    card.addEventListener("click", open);
    card.addEventListener("keydown", event => {
      if (event.key === "Enter" || event.key === " ") { event.preventDefault(); open(); }
    });
  });
}

function openProject(id) {
  const project = config.projects.find(item => item.id === id);
  if (!project) return;
  const content = project[language];
  modal.className = `modal open accent-${project.accent}`;
  modal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
  document.getElementById("modalVisual").className = `modal-visual accent-${project.accent}`;
  document.getElementById("modalKicker").textContent = content.label;
  document.getElementById("modalTitle").textContent = content.title;
  document.getElementById("modalSummary").textContent = content.subtitle;
  document.getElementById("modalChallengeText").textContent = content.challenge;
  document.getElementById("modalSolutionText").textContent = content.solution;
  document.getElementById("modalContributionList").innerHTML = content.contribution.map(item => `<li>${item}</li>`).join("");
  document.getElementById("modalFeatureList").innerHTML = content.features.map(item => `<li>${item}</li>`).join("");
  document.getElementById("modalStack").innerHTML = project.stack.map(item => `<span>${item}</span>`).join("");
  updateTranslations();
  setTimeout(() => modal.querySelector(".modal-close").focus(), 50);
}

function closeModal() {
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
}

document.querySelectorAll("[data-close-modal]").forEach(item => item.addEventListener("click", closeModal));
document.addEventListener("keydown", event => { if (event.key === "Escape" && modal.classList.contains("open")) closeModal(); });

function applyFilter(filter) {
  activeFilter = filter;
  document.querySelectorAll(".project-card").forEach(card => {
    const visible = filter === "all" || card.dataset.category.split(" ").includes(filter);
    card.classList.toggle("hidden", !visible);
  });
  document.querySelectorAll(".filter-button").forEach(button => button.classList.toggle("active", button.dataset.filter === filter));
}
document.querySelectorAll(".filter-button").forEach(button => button.addEventListener("click", () => applyFilter(button.dataset.filter)));

function updateTranslations() {
  document.documentElement.lang = language === "idn" ? "id" : "en";
  document.querySelectorAll("[data-i18n]").forEach(node => {
    const key = node.dataset.i18n;
    if (translations[language][key]) node.textContent = translations[language][key];
  });
  document.getElementById("languageToggle").textContent = language === "en" ? "ID" : "EN";
  localStorage.setItem("portfolio-language", language);
}

document.getElementById("languageToggle").addEventListener("click", () => {
  language = language === "en" ? "idn" : "en";
  updateTranslations();
  renderProjects();
  if (modal.classList.contains("open")) closeModal();
});

const root = document.documentElement;
const savedTheme = localStorage.getItem("portfolio-theme");
if (savedTheme) root.dataset.theme = savedTheme;
function updateThemeIcon() { document.querySelector(".theme-icon").textContent = root.dataset.theme === "dark" ? "☼" : "☾"; }
updateThemeIcon();
document.getElementById("themeToggle").addEventListener("click", () => {
  root.dataset.theme = root.dataset.theme === "dark" ? "light" : "dark";
  localStorage.setItem("portfolio-theme", root.dataset.theme);
  updateThemeIcon();
});

const menuButton = document.getElementById("menuToggle");
const mobilePanel = document.getElementById("mobilePanel");
menuButton.addEventListener("click", () => {
  const open = mobilePanel.classList.toggle("open");
  menuButton.setAttribute("aria-expanded", String(open));
  mobilePanel.setAttribute("aria-hidden", String(!open));
});
mobilePanel.querySelectorAll("a").forEach(link => link.addEventListener("click", () => {
  mobilePanel.classList.remove("open"); menuButton.setAttribute("aria-expanded", "false"); mobilePanel.setAttribute("aria-hidden", "true");
}));

function observeReveals() {
  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => { if (entry.isIntersecting) { entry.target.classList.add("visible"); observer.unobserve(entry.target); } });
  }, { threshold: .12 });
  document.querySelectorAll(".reveal:not(.visible)").forEach(item => observer.observe(item));
}

function setupLinks() {
  const { email, linkedin, github } = config.profile;
  const emailLink = document.getElementById("emailLink");
  emailLink.href = `mailto:${email}`;
  [["linkedinLink", linkedin], ["githubLink", github]].forEach(([id, url]) => {
    const link = document.getElementById(id);
    if (url) { link.href = url; link.classList.remove("disabled-link"); link.removeAttribute("aria-disabled"); link.target = "_blank"; link.rel = "noreferrer"; }
    else link.addEventListener("click", event => event.preventDefault());
  });
}

updateTranslations();
renderProjects();
setupLinks();
observeReveals();
